package com.eleven.bean;

/**
 * @ClassName Temperature
 * @Description 用来获取和存储天气数据的类
 * @Author ELeven
 * @Date 2022/4/19 21:08
 * @Version 1.0
 **/
public class Temperature {
    /**
     * 低温数组
     */
    public String lows;
    /**
     * 高温数组
     */
    public String highs;
    /**
     * 时间数组
     */
    public String times;
    //keyLow 和 maxLow 组成了最低温点
    /**
     * 最低温x轴
     */
    public String keyLow;
    /**
     * 最低温y轴
     */
    public String maxLow;

}
